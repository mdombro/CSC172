public class Point {
    double x, y;
    Point (double x1, double y1) {
        x = x1;
        y = y1;
    }

    public double getx() {
        return x;
    }

    public double gety() {
        return y;
    }

    public void setPoint(double x1, double y1) {
        x = x1;
        y = y1;
    }
}
